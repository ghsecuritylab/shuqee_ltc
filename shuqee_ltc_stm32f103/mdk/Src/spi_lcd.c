//向液晶屏写一个8位指令
void Lcd_WriteIndex(uint8_t Index)
{
	//SPI 写命令时序开始
	LCD_CS_CLR();
	LCD_DC_CLR();
	spi1_write_byte(Index);
	LCD_CS_SET();
}

//向液晶屏写一个8位数据
void Lcd_WriteData(uint8_t Data)
{
	LCD_CS_CLR();
	LCD_DC_SET();
	spi1_write_byte(Data);
	LCD_CS_SET(); 
}
//向液晶屏写一个16位数据
void LCD_WriteData_16Bit(uint16_t Data)
{
	LCD_CS_CLR();
	LCD_DC_SET();
	spi1_write_byte(Data>>8); 	//写入高8位数据
	spi1_write_byte(Data); 			//写入低8位数据
	LCD_CS_SET(); 
}

void Lcd_WriteReg(uint8_t Index,uint8_t Data)
{
	Lcd_WriteIndex(Index);
	Lcd_WriteData(Data);
}

void Lcd_Reset(void)
{
	LCD_RST_CLR();
	delay_ms(100);
	LCD_RST_SET();
	delay_ms(50);
}

